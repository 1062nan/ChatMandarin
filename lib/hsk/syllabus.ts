/**
 * 获取指定级别的 HSK 大纲
 */
export function getHSKSyllabus(level: number): HSKSyllabus {
  return HSK_SYLLABUS.find(s => s.level === level) || HSK_SYLLABUS[2] // 默认 HSK 3
}

/**
 * 获取该级别 + 话题簇对应的"内容生成 prompt 增强片段"
 * 用于 content-generator 注入到 AI prompt
 */
export function getSyllabusPromptContext(
  level: number,
  topic?: string
): string {
  const syllabus = getHSKSyllabus(level)

  // 从话题簇中根据 topic 关键词挑选最相关的 3-5 个
  let relevantTopics = syllabus.topicClusters
  if (topic) {
    const topicLower = topic.toLowerCase()
    const matched = syllabus.topicClusters.filter(t =>
      t.toLowerCase().includes(topicLower) ||
      topicLower.includes(t.split('（')[0])
    )
    if (matched.length > 0) relevantTopics = matched
  }

  // 选 6-8 个最重要的语法点
  const keyGrammar = syllabus.grammarPoints.slice(0, 8)

  // 选 15-20 个代表性词
  const sampleVocab = syllabus.sampleWords.slice(0, 20)

  return `
=== HSK ${level} SYLLABUS ALIGNMENT ===

Target vocabulary count: ${syllabus.vocabularyCount} words

Grammar points THIS level should actively practice:
${keyGrammar.map(g => `- ${g}`).join('\n')}

Topic clusters (use these as content themes):
${relevantTopics.slice(0, 5).map(t => `- ${t}`).join('\n')}

Sample vocabulary at THIS level (must include where natural):
${sampleVocab.join('、')}

What students at HSK ${level} can do:
${syllabus.examSkills.map(s => `- ${s}`).join('\n')}

Content generation rules:
1. Use ONLY vocabulary at HSK ${level} and below (cumulative).
2. ACTIVELY use 3-5 of the grammar points above in the dialogue/sentences.
3. Stay within the topic clusters — don't drift to advanced-only topics.
4. If a concept requires higher-level vocabulary, rephrase using HSK ${level} alternatives.
`.trim()
}

/**
 * 获取 HSK 考试题型描述（用于 HSK 备考模式的 UI）
 */
export function getExamFormatDescription(level: number): string {
  const syllabus = getHSKSyllabus(level)
  return [
    `【听力】${syllabus.examFormat.listening}`,
    `【阅读】${syllabus.examFormat.reading}`,
    syllabus.examFormat.writing ? `【写作】${syllabus.examFormat.writing}` : '',
  ].filter(Boolean).join('\n')
}

