/**
 * 获取指定级别的 HSK Profile
 */
export function getHSKProfile(level: number): HSKLevelProfile {
  return HSK_LEVELS.find(l => l.level === level) || HSK_LEVELS[2] // 默认 HSK 3
}

/**
 * 获取词汇难度提示（附加到 system prompt）
 */
export function getVocabularyGuidance(level: number): string {
  const profile = getHSKProfile(level)
  const lowerProfiles = HSK_LEVELS.filter(l => l.level <= level)

  // 合并所有可用词汇
  const allAvailableWords = lowerProfiles.flatMap(p => p.coreWords)

  // 必须避免的词汇（高级词）
  const avoidList = profile.avoidWords.join('、')

  // 核心特征
  const grammarFocus = profile.grammarFocus.join('、')

  return `
=== VOCABULARY GUIDANCE (HSK ${level}) ===
Vocabulary count: ${profile.vocabularyCount} words
Grammar patterns available: ${grammarFocus}

AVOID these words (too advanced for HSK ${level}):
${avoidList}

The student can handle:
${profile.canDo.map(c => `- ${c}`).join('\n')}

Remember: Keep your Chinese natural but accessible. If you need to express a concept
that requires higher-level vocabulary, find a simpler way using HSK ${level} and below.
`
}
